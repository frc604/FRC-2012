package com._604robotics.robot2012.Aiming;

public class Point3d {
    public double x, y, z;

    public Point3d() {
        
    }

    /**
     * @param x
     * @param y
     * @param z
     */
    public Point3d(double x, double y, double z) {
        super();
        this.x = x;
        this.y = y;
        this.z = z;
    }
}