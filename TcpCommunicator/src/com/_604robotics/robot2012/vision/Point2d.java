package com._604robotics.robot2012.vision;


public class Point2d {
	double x, y;

	/**
	 * @param x
	 * @param y
	 */
	public Point2d(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}

	
	public double getX() {
		return x;
	}

	
	public void setX(double x) {
		this.x = x;
	}

	
	public double getY() {
		return y;
	}

	
	public void setY(double y) {
		this.y = y;
	}
	
	
}
